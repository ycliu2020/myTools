function [ r95 ] = R95p( n ,opre )
%   求极端降水阈值r95=具体的阈值
%必须先排序
%   opre为三维数组，第三维为时间，前两位为经纬度
%   n是总天数
s=0.95*(n+0.38)+0.31;
if rem(s,1)==0
   r95=opre(:,:,s);
else
  p1=(fix(s)-0.31)/(n+0.38);
  p2=(fix(s+1)-0.31)/(n+0.38);
  s1=fix(s);
  %线性插值（求线性方程）
  k=(opre(:,:,s1)-opre(:,:,s1+1))/(p1-p2);
  b=opre(:,:,s1+1)-k*p2;
  r95= k*0.95+b;
end

end
   
